package Part2.Moreloops;

import java.util.Scanner;

public class Counting {
    public static void main(String[] args) {
        Scanner sca=new Scanner(System.in);
        System.out.println("enter number");
        int num= sca.nextInt();;
        for (int i=0;i<=num;i++){
            System.out.println(i);
        }
    }
}
